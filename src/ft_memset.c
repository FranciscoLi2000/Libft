/* ft_memset.c - Fill memory with a constant byte */
/* Description: Fills the first n bytes of the memory area pointed to by s with the constant byte c */

#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	*ptr;

	i = 0;
	ptr = (unsigned char *)s;
	while (i < n)
	{
		ptr[i] = (unsigned char)c;
		i++;
	}
	return (s);
}
